package com.siit.xml.dtos;

public class AuthorDTO {
	private String authorsName;
	private String authorsEmail;
	private String authorsPhone;
	private String authorsAddress;
	
	public String getAuthorsName() {
		return authorsName;
	}
	public void setAuthorsName(String authorsName) {
		this.authorsName = authorsName;
	}
	public String getAuthorsEmail() {
		return authorsEmail;
	}
	public void setAuthorsEmail(String authorsEmail) {
		this.authorsEmail = authorsEmail;
	}
	public String getAuthorsPhone() {
		return authorsPhone;
	}
	public void setAuthorsPhone(String authorsPhone) {
		this.authorsPhone = authorsPhone;
	}
	public String getAuthorsAddress() {
		return authorsAddress;
	}
	public void setAuthorsAddress(String authorsAddress) {
		this.authorsAddress = authorsAddress;
	}
	
	

}
